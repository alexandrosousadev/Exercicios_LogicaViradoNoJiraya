

public class ReutilizandoVariaveis {
    public static void main(String[] args) {
        double salario = 5000;

        double resultado = salario * 0.3;


        System.out.println("O resultado de 30% foi " + resultado );
        resultado = salario * 0.15;
        System.out.println("O resultado de 15% foi " + resultado );
        resultado = salario * 0.05;
        System.out.println("O resultado de 5% foi " + resultado );





    }
}
